export const getAthleteById = (id: string): Athlete | undefined =>
  athletes.find((a) => a.id === id);

export const getAthletesByEvent = (event: string): Athlete[] =>
  athletes.filter((a) => a.events.includes(event));

export const getAthletesByCountry = (country: string): Athlete[] =>
  athletes.filter((a) => a.country === country);

export const getAthletesByGender = (gender: 'male' | 'female'): Athlete[] =>
  athletes.filter((a) => a.gender === gender);

export const getSimilarAthletes = (id: string): Athlete[] => {
  const athlete = getAthleteById(id);
  if (!athlete) return [];
  return athlete.similarAthleteIds
    .map((sid) => getAthleteById(sid))
    .filter((a): a is Athlete => a !== undefined);
};

export const getAllCountries = (): string[] => {
  const countries = new Set(athletes.map((a) => a.country));
  return Array.from(countries).sort();
};

export const getAllEvents = (): string[] => {
  const events = new Set(athletes.flatMap((a) => a.events));
  return Array.from(events).sort();
};

export const searchAthletes = (query: string): Athlete[] => {
  const q = query.toLowerCase();
  return athletes.filter(
    (a) =>
      a.name.toLowerCase().includes(q) ||
      a.englishName.toLowerCase().includes(q) ||
      a.country.toLowerCase().includes(q) ||
      a.events.some((e) => e.toLowerCase().includes(q)) ||
      a.tags.some((t) => t.toLowerCase().includes(q))
  );
};
